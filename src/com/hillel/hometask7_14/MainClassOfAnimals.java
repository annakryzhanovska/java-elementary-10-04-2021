package com.hillel.hometask7_14;
import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class MainClassOfAnimals {
    public static void main(String[] args) throws IOException {
        System.out.println("create an animal: tiger, or cat, or dog, or duck");
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        while(true){
            String string = reader.readLine();
            if(string.equals(" ")){
                break;
            }
            handleAnimal(createAnimal(string));
        }
        }

        public static Animal createAnimal(String string){
            Animal animal = null;
        switch(string.toLowerCase().trim()){
            case "cat":
                animal = new Cat("Sam");
                break;
            case "dog" :
                animal = new Dog("Bob");
                break;
            case "tiger":
                animal = new Tiger("Teddy");
                break;
            case "duck":
                animal = new Duck("Molly");
                break;
            default:
                System.out.println("create an animal one more time pls: tiger, or cat, or dog, or duck");
                break;
        }
            return animal;
        }

        public static void handleAnimal(Animal animal){
        if(animal instanceof Runnable){
            ((Runnable) animal).run();
        }
        if(animal instanceof Climbable){
            ((Climbable) animal).climb();
        }
        if(animal instanceof Flyable){
            ((Flyable) animal).fly();
        }

        }
}

