package com.hillel.hometask7_14;

public class Cat extends Animal implements Runnable, Climbable{
    String name;
    public Cat(String name){
        this.name = name;
    }
    @Override
    public void climb() {
        System.out.println("Cat " + name + " climbs");
    }

    @Override
    public void run() {
        System.out.println("Cat " + name + " runs");
    }
}
