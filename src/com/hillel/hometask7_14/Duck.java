package com.hillel.hometask7_14;

public class Duck extends Animal implements Flyable, Runnable, Climbable {
    String name;
    public Duck(String name){
        this.name = name;
    }
    @Override
    public void climb() {
        System.out.println("Duck " + name + " climbs");
    }

    @Override
    public void fly() {
        System.out.println("Duck " + name + " flies");
    }

    @Override
    public void run() {
        System.out.println("Duck " + name + " runs(?). Yes, actually it can");
    }
}
