package com.hillel.hometask7_14;

public class Dog extends Animal implements Runnable, Climbable{
    String name;
    public Dog(String name){
        this.name = name;
    }
    @Override
    public void climb() {
        System.out.println("Dog " + name + " climbs");
    }

    @Override
    public void run() {
        System.out.println("Dog " + name + " runs");
    }
}
